public class Main {
    public static void main(String[] args) {
        // Deklarasi dan inisialisasi variabel bertipe byte
        byte angka1 = 125;
        byte angka2 = 6;

        // Melakukan penjumlahan dan casting ke tipe byte
        byte hasil = (byte) (angka1 + angka2);

        // Mencetak hasil ke konsol
        System.out.println("Hasil 1 = " + hasil);
    }
}